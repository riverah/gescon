<h2>Filosofia</h2>
<h1>¿Que es la red gescon?</h1>
<p>
Una alianza multi-actores comprometidos con el desarrollo rural territorial formada por<br>
personas naturales y jurídicas, organizados en red con el fin de impulsar la gestión del<br>
conocimiento en función de mejorar las condiciones de vida en los territorios rurales de<br>
Matagalpa y Jinotega.<br>

Interactúan profesionales a título personal y organizaciones públicas y privadas con potencial<br>
para proponer e incidir en las políticas públicas, influir en los procesos de reformas<br>
curriculares a nivel de la educación técnica media y superior, mejora el desempeño del<br>
tejido organizacional presente en los territorios con metodologías, técnicas y herramientas<br>
aplicadas a los distintos contextos, da valor científico al conocimiento empírico generado<br>
por las familias rurales, actuando en sinergias maximiza las oportunidades y disminuye los<br>
traslapes en la gestión de los actores locales y organizaciones e instituciones de apoyo, que<br>
trabajan en los territorios desde el enfoque de desarrollo rural territorial.<br>
</p>
