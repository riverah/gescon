<h1>¿Como somos?</h1>
<h2>nuestra cultura organizacional</h2>
<p>
Entendiendo que la Red GESCON es un espacio de articulación de diferentes actores<br>
que promueven el Desarrollo Rural y que está orientada específicamente a la Gestión de<br>
Conocimientos, podemos decir que se pondrá de manifiesto un sistema de convicciones,<br>
creencias y valores aseguramos entonces que en la Red. . .
</p>
<li>
Es un espacio abierto y dinámico de gestión del conocimiento que contribuye a la<br>
toma de decisiones, poniendo resultados útiles y confiables en manos de las familias<br>
rurales, organismos e instituciones miembros, profesionales y sociedad en general<br>
que favorezcan de modo significativo los procesos de desarrollo rural territorial.
</li>
<li>
Generamos información pertinente para la toma de decisiones, mejora de los sistemas<br>
productivos, agroindustriales y organizacionales basada en las experiencias y realidades<br>
de los territorios mediante la retroalimentación, ordenamiento y planificación del<br>
territorio, innovación, inclusión social, equidad de género e incidencia en las políticas<br>
públicas territoriales.
</li>
<li>
Trabajamos, creemos y nos desarrollamos mujeres y hombre a título personal, organizaciones<br>
e instituciones, entre ellos la academia y el sector productivo.
</li>
<li>
La Red GESCON está en su etapa inicial dando un paso significativo hacia la adolescencia<br>
teniendo como instrumento guía el presente Plan Estratégico.
</li>
<li>
Aportamos conocimientos y metodologías, el saber y el saber hacer para vivir en
armonía con la naturaleza.
</li>
<li>
Estamos integrados por diferentes organizaciones, personas e instituciones que cuentan<br>
con diferentes redes de apoyo y amistades que han construido a lo largo de los
años.
</li>
<li>
Respetamos la diversidad de pensamientos políticos, espirituales, religiosos y culturales,<br>
así como la identidad de cada una de las organizaciones, instituciones y personas naturales,<br>
miembros de la Red.
</li>
<li>
La Red es altamente proactiva, dinámica y emprendedora, con comunicación fluida,<br>
sabe equilibrar el comportamiento formal e informal está orientada a fortalecer la<br>
articulación entre actores.
</li>
<li>
Nos caracteriza el activismo, la voluntariedad, la capacidad de gestión. Hay oportunidad<br>
para la creatividad, la innovación y la generación de conocimientos.
</li>
<li>
Nuestra forma de funcionamiento es sencilla y organizada aportamos al desarrollo<br>
rural con enfoque de Territorios Climáticamente Inteligente (TCI), Género y Generacional.
</li>
<h2>Nuestras principales fortalezas.</h2>
<h3>Identidad</h3>
<p>
Espacio donde es posible dialogar sobre desarrollo rural desde varias experiencias y<br>
conocimientos con enfoques relevantes.
</p>
<h3>Estructura y Organización</h3>
<p>
Somos una plataforma multiactor con una coordinación apoyada por un equipo<br>
de apoyo a la coordinación elegido democráticamente en asamblea de miembros.<br>
Trabajamos con la modalidad de comisiones de trabajos.
</p>
<h3>Toma de decisiones colegiadas</h3>
<p>
La Asamblea General es el máximo órgano de decisión en donde se aprueban planes,
convenios, nuevas membrecías y cambios a tomar.
</p>
<h3>Políticas, Estrategias y Planes</h3>
<p>
Desarrollamos un proceso de planificación estratégica y nos proponemos trabajar<br>
con planes operativos anuales orientada a resultados. Contamos con instrumento<br>
para la formalización de la incorporación a la Red. Establecemos alianzas con otras<br>
plataformas locales (AMUPNOR)
</p>
<h3>Personas, Grupos y Clima, nuestros métodos y procedimientos</h3>
<p>
La membrecía se caracteriza por una amplia capacidad de generación de conocimientos
y con voluntad para aportar esos conocimientos en pro del desarrollo de los
territorios, a la vez que se promueven espacios de diálogo y concertación para la
gestión del conocimiento.
</p>
<h3>El equipo de la Red GESCON y su compromiso</h3>
<p>
Por la característica de Red, contamos con un equipo multidisciplinario con alto<br>
sentido de voluntariado y altruismo, que participan a título personal o en nombre de<br>
organizaciones e instituciones comprometidas con el desarrollo rural, incorporan en<br>
su quehacer cotidiano enfoques de medio-ambiente, empoderamiento de género y<br>
generacional.
</p>
<h3>Las Metodologías de trabajo</h3>
<p>
Se integran una amplia variedad de saberes, prácticas y aprendizajes de procesos<br>
desarrollados desde las personas, instituciones y organizaciones que conforman este<br>
espacio, así mismo asumimos desde la Red GESCON el compromiso de ir creando en<br>
el marco de implementación del presente PE, un acervo propio con identidad de la<br>
RED.
</p>
<h3>La Capacidad de Gestión</h3>
<p>
Promovemos la autogestión de recursos, bines y servicios con el fin de fortalecer<br>
a las organizaciones e instituciones miembros
</p>
<h3>El respeto al medioambiente</h3>
<p>
Como un valor fundamental de la red, es imperativo mantener este enfoque<br>
en los diferentes quehaceres.
</p>
<h3>Nuestra Diferenciación</h3>
<p>
Incorporamos en nuestro enfoque el concepto de inteligencia climática, la equidad<br>
de género y generacional al desarrollo rural territorial sobre la base del trabajo en<br>
equipo y el voluntariado entre los miembros de la red articulando la academia, el<br>
sector productivo, la sociedad civil y el estado.
</p>
<h3>Nuestro enfoque</h3>
<p>
Nuestro enfoque es el territorio, conceptualizado como una construcción<br>
social que siente, piensa e interactúa con identidad propia.
</p>
