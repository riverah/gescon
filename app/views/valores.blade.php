<h1>Nuestros valores</h1>
<h2>Nuestros valores finales</h2>
<p>
Los valores finales expresan lo que es importante para cada persona, organización e institución<br>
miembro de la Red GESCON, de lo que queremos para las familias rurales.
</p>
<h2>Valores personales</h2>
<h3>Aportar conocimientos</h3>
<p>
Para GESCON el aporte de conocimientos es la razón de ser de la red, por lo cual<br>
estamos convencidos que ésta es un práctica constante en el qué hacer de ésta plataforma.<br>
Esto implica entre otras acciones la aprobación y aplicación de prácticas como:<br>
generación de información, compartir y difundir información y retroalimentación del<br>
conocimiento entre los miembros de la red.
</p>
<h2>Valores éticos-sociales</h2>
<h3>Bienestar de la familia</h3>
<p>
Implica la mejora constante de la calidad de vida de las familias rurales impulsando
innovaciones tecnológicas, enfocadas a darle valor agregado a la producción, cuidando
a la vez sus propios recursos naturales y facilitando el acceso a los servicios
básicos. Esto implica desarrollar capacidades de gestión, organización y administración
de recursos e identificación, análisis y selección de alternativas viables de
comercialización.
</p>
<h3>Equidad de genero</h3>
<p>
Las acciones tanto a lo interno de la red como las realizadas en los territorios deben<br>
garantizar el espacio y el balance necesario entre hombres y mujeres para equiparar<br>
las relaciones de poder en los procesos de participación, toma de decisiones, acceso<br>
y control de recursos y tecnología. La equidad de género vista como una categoría<br>
de análisis es de suma importancia para la red porque promueve la participación<br>
activa de las mujeres en el desarrollo rural y permite la toma de conciencia de la<br>
importancia de las relaciones armoniosas entre hombres y mujeres a lo interno de<br>
la familia, influyendo directamente en la disminución de la violencia basada en las<br>
diferencias de género. Esto implica la apropiación y aplicación de prácticas como:<br>
sensibilización en género, medidas afirmativas de reconocimiento del aporte de las<br>
mujeres al desarrollo rural territorial, promoción de la participación de las mujeres en<br>
espacios de toma de decisiones y fomento del acceso y control de recursos que las<br>
empoderen como sujetos de desarrollo procurando al mismo tiempo la justa división<br>
del trabajo en el hogar.
</p>
<h2>Nuestros valores operativos</h2>
<h3>Honestidad y responsabilidad</h3>
<p>
Estamos convencidos que la reafirmación de este valor en cada una de las actuaciones<br>
de la red confirma una imagen de transparencia, ganamos legitimidad y nos hace<br>
más confiables como actores de desarrollo. Esto implica la apropiación y aplicación<br>
sana de prácticas como rendición de cuentas a lo interno de la red, el territorio,<br>
autoridades y sociedad en general de los resultados sociales, económicos, financieros y<br>
ambientales. Es importante también contar con una sólida capacidad de representación,<br>
una coherencia compacta y visible en todo momento y rectitud en las acciones,<br>
servicios y compromisos asumidos.
</p>
<h3>Trabajo en equipo</h3>
<p>
Es nuestra convicción que la planificación, ejecución y evaluación de las acciones de<br>
la red en función del desarrollo rural territorial, bajo la forma de equipos de trabajo,<br>
promueve el consenso, el espíritu de colaboración y confianza mutua. La búsqueda de<br>
sinergia y mayor productividad impone la necesidad de apropiarnos de prácticas de<br>
solidaridad al asumir las responsabilidades asignadas, igualmente enfocarnos en el<br>
apoyo equitativo y recíproco para alcanzar resultados de calidad en menos tiempo y<br>
al menor costo.
</p>
<h3>Innovación tecnológica</h3>
<p>
Mantener una actitud abierta al cambio, a la transformación tecnológica y al desarrollo<br>
de nuevas técnicas de producción. En este esfuerzo es vital orientar el proceso de toma<br>
de decisiones a maximizar los recursos productivos de los territorios sin maltratar el<br>
medio ambiente. Desarrollar capacidades en el uso de las TIC para mejorar el acceso a<br>
mercados, compartir información y tareas a lo interno y externo de la red. El desarrollo<br>
de planes agro productivos sustentables encaminados a mejorar la productividad de<br>
los territorios es para la red una forma eficiente y efectiva para gestionar conocimiento<br>
a nivel de territorios y organizaciones miembros comprometidas con el desarrollo rural.
</p>
<h3>Actitud positiva y liderazgo</h3>
<p>
Significa que debemos asumir los desafíos como oportunidades de aprendizaje y<br>
crecimiento fortaleciendo la participación activa y constante de la membresía y los<br>
actores territoriales de manera cohesionada frente a objetivos comunes procurando<br>
en todo momento desarrollar confianza e identidad de red. Implica además, trabajar<BR>
con espíritu colaborativo reconociendo las habilidades, destrezas y conocimientos de<br>
los diversos actores del desarrollo mediante la implementación de mecanismos de<br>
comunicación asertiva, efectiva y oportuna
</p>
