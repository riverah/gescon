<H1><b>Un breve brochazo de nuestra historia</b></H1>
  <img src="img/reseña.jpg">
  <p>
    La Red GESCON para la zona centro norte, nace como iniciativa de un grupo de profesionales
quienes allá por el año 2008 cursaban estudios de maestría en el tema de Desarrollo
Rural Territorial Sustentable, auspiciado este curso por el Colegio de Post Graduados de
la ciudad de Puebla, México y la Universidad Nacional Autónoma de Nicaragua, Núcleo
Regional “Mariano Fiallos Gil”. En ese entonces se discutía y analizaba en los distintos
módulos, la situación por la que atravesaba el desarrollo rural de nuestra región y en el
contexto de ese análisis y discusiones se planteaba que era necesario que ese curso de
maestría generara además de los graduados, otros productos más tangibles y fue así que ese
grupo de maestrantes se propusieron generar algunos productos además de coronar con la
graduación el esfuerzo académico.
  </p>
  <h2><b>¿Como nacimos?</b></h2>
  <p>
    Congresos Nacionales de Desarrollo Rural, espacio en el que convergen la academia, los
productores, el gobierno y otros sectores. Se crean proyectos de desarrollo y se incide para
que la UNAN FAREM Matagalpa los avalara por medio de la Red.<br>
Esta etapa se caracterizo por desarrollar encuentros con organizaciones. Análisis de la
problemática del Desarrollo Rural en la zona. Intercambios de experiencias en desarrollo
rural. Se da la participación de autoridades superiores de la UNAN-Managua en los eventos.
De igual forma en ese mismo año se definieron los objetivos y normativa básica para la
gobernanza de la Red de Gestión del Conocimiento para el Desarrollo Rural de Matagalpa
y Jinotega. <br>

En el año 2012, se Funda la Red de Gestión del Conocimiento para el Desarrollo<br>
 Rurral de Matagalpa y Jinotega, en la que participaron 57 actores.<br>

 En este acto se aprobó el plan de actividades de la Red correspondiente al año 2012,
entre las que destacan la construcción de la agenda de investigación, y de una plataforma
virtual que ayude al intercambio de Información generada por los miembros de la Red.

En ese año se definen los valores y líneas de acción.Se refuerza la participación de actores
comprometidos en argumentar la pertinencia de integrar el enfoque de género.<br>

GESCON forma parte de la Comisión Nacional de Desarrollo Rural C.N.D.R. Se da alianza
con el Estado para organizar y desarrollar el 1er Congreso de Innovación Tecnológica.
  </p>
  <h2 >¿Qué es la Red GESCON?</h2>
  <p >
    Una alianza multi-actores comprometidos con el desarrollo rural territorial formada por
personas naturales y jurídicas, organizados en red con el fin de impulsar la gestión del
conocimiento en función de mejorar las condiciones de vida en los territorios rurales de
Matagalpa y Jinotega.<br><br>


Interactúan profesionales a título personal y organizaciones públicas y privadas con potencial
para proponer e incidir en las políticas públicas, influir en los procesos de reformas
curriculares a nivel de la educación técnica media y superior, mejora el desempeño del
tejido organizacional presente en los territorios con metodologías, técnicas y herramientas
aplicadas a los distintos contextos, da valor científico al conocimiento empírico generado
por las familias rurales, actuando en sinergias maximiza las oportunidades y disminuye los
traslapes en la gestión de los actores locales y organizaciones e instituciones de apoyo, que
trabajan en los territorios desde el enfoque de desarrollo rural territorial.<br>

Crear un espacio abierto y dinámico de gestión del conocimiento que contribuye a la toma
de decisiones, poniendo resultados útiles y confiables en manos de las familias rurales,
organismos e instituciones miembros, profesionales y sociedad en general que favorezcan
de modo significativo los procesos de desarrollo rural territorial.<br>
<br>
Las Organizaciones Miembros de la Red GESCON trabajamos para contribuir al desarrollo
rural en aquellos territorios en los que tenemos presencia, en función de mejorar la calidad
de vida de las familias rurales.
  </p>
<h2 >Nuestra misión</h2>
<img src="img/mision.jpg">
<br>
<p>
La red GESCON es una alianza dinámica entre academia, estado, sociedad civil y sector
productivo, que gestiona conocimientos sobre metodologías, investigación y opciones
tecnológicas con enfoque de desarrollo rural territorial.
</p>

<h2>Nuestra visión al 2025</h2>
<img src="img/vision.jpg"><br>
Familias rurales y organizaciones comprometidas con el desarrollo territorial toman decisiones
basadas en la gestión de conocimientos con conciencia ecológica, de género y
generacional.

<h2>Nuestra visión de la organización como plataforma</h2>
La red de Gestión del Conocimiento se posiciona como una plataforma científica de incidencia
al servicio del desarrollo rural territorial de los departamentos de Matagalpa y
Jinotega.

